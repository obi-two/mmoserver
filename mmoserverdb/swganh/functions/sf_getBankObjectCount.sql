DROP FUNCTION IF EXISTS `swganh`.`sf_getBankObjectCount`;

DELIMITER $$

/*!50003 SET @TEMP_SQL_MODE=@@SQL_MODE, SQL_MODE='NO_AUTO_VALUE_ON_ZERO' */ $$

CREATE FUNCTION `sf_getBankObjectCount`(bankId BIGINT, mplanetID INT(2)) RETURNS int(11) SQL SECURITY INVOKER
BEGIN
  DECLARE containerCount INT;
  DECLARE itemCount INT;
  DECLARE resourceContainerCount INT;
  DECLARE totalCount INT;
  DECLARE BankPlanet INT(2);

  SELECT COUNT(*) FROM banks WHERE id = bankId AND planet_id = mplanetID INTO BankPlanet;

    IF (BankPlanet = 1) THEN

      SELECT COUNT(containers.id) FROM containers WHERE (parent_id = bankId) INTO containerCount;
      SELECT COUNT(items.id) FROM items WHERE (parent_id = bankId) INTO itemCount;
      SELECT COUNT(resource_containers.id) FROM resource_containers WHERE (parent_id = bankId) INTO resourceContainerCount;
      SET totalCount = (containerCount + itemCount + resourceContainerCount);

      RETURN totalCount;

    ELSE set totalCount = 0;

    RETURN totalCount;

    END IF;

END $$
